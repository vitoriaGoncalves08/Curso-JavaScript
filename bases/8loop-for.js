//FOR
for(let i = 5; i >= 1; i--){
    if(i % 2 !== 0){
        console.log(i);
    }
}

//WHILE LOOP
let i = 5;

while(i >= 1){
    if(i % 2 !== 0){
        console.log(i);
    }
    i--;
}